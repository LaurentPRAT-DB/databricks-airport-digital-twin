# ── Lookup helpers ────────────────────────────────────────────────


def get_airport(iata: str) -> Optional[tuple[float, float, str, str]]:
    """Look up airport by IATA code. Returns (lat, lon, icao, country) or None."""
    return AIRPORTS.get(iata)


def get_coords(iata: str) -> Optional[tuple[float, float]]:
    """Look up airport coordinates by IATA code. Returns (lat, lon) or None."""
    entry = AIRPORTS.get(iata)
    return (entry[0], entry[1]) if entry else None


def get_country_name(iata: str) -> str:
    """Look up country name for an airport IATA code."""
    entry = AIRPORTS.get(iata)
    if entry:
        return COUNTRY_NAMES.get(entry[3], entry[3])
    return "Unknown"


def get_icao(iata: str) -> Optional[str]:
    """Look up ICAO code for an IATA code."""
    entry = AIRPORTS.get(iata)
    return entry[2] if entry else None


def haversine_km(lat1: float, lon1: float, lat2: float, lon2: float) -> float:
    """Great-circle distance between two points in kilometres."""
    R = 6371.0
    dlat = math.radians(lat2 - lat1)
    dlon = math.radians(lon2 - lon1)
    a = (math.sin(dlat / 2) ** 2
         + math.cos(math.radians(lat1)) * math.cos(math.radians(lat2))
         * math.sin(dlon / 2) ** 2)
    return R * 2 * math.atan2(math.sqrt(a), math.sqrt(1 - a))


def find_nearby(
    iata: str,
    max_distance_km: float = 3000.0,
    min_distance_km: float = 200.0,
) -> tuple[list[str], list[str]]:
    """
    Find airports near the given airport, split into nearby and far.

    Args:
        iata: Reference airport IATA code
        max_distance_km: Max distance for "nearby" bucket (default 3000km)
        min_distance_km: Min distance to exclude very close airports

    Returns:
        (nearby, far) — lists of IATA codes
    """
    ref = AIRPORTS.get(iata)
    if not ref:
        # Unknown airport — return all as far
        all_codes = [c for c in AIRPORTS if c != iata]
        return [], all_codes

    ref_lat, ref_lon = ref[0], ref[1]
    ref_country = ref[3]
    nearby: list[str] = []
    far: list[str] = []

    for code, (lat, lon, _icao, country) in AIRPORTS.items():
        if code == iata:
            continue
        d = haversine_km(ref_lat, ref_lon, lat, lon)
        if d < min_distance_km:
            continue
        if d <= max_distance_km or country == ref_country:
            nearby.append(code)
        else:
            far.append(code)

    return nearby, far

